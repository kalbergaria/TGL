////////////////////////////////////////////////////////////////////////////////////////////////////
// EXTERNAL INCLUDES:
////////////////////////////////////////////////////////////////////////////////////////////////////
#include <stdio.h>
#include <unistd.h>

////////////////////////////////////////////////////////////////////////////////////////////////////
// LOCAL INCLUDES:
////////////////////////////////////////////////////////////////////////////////////////////////////
#include "TLib/TLib.h"

////////////////////////////////////////////////////////////////////////////////////////////////////
// CONSTANTS:
////////////////////////////////////////////////////////////////////////////////////////////////////
const char BORDER_CHAR = '*';
const char* START_TEXT = "PRESS ANY BUTTON TO START!";

////////////////////////////////////////////////////////////////////////////////////////////////////
// MAIN:
////////////////////////////////////////////////////////////////////////////////////////////////////
int main()
{
	// Get the height and width of the terminal
	const unsigned short TERM_HEIGHT = TLib::getTerminalHeight();
	const unsigned short TERM_WIDTH = TLib::getTerminalWidth();

	// Check that the terminal meets any minimum size requirements
	TLib::validateTerminalSize(TERM_HEIGHT, TERM_WIDTH);

	// Clear the terminal
	TLib::clearTerminal();

	printf("knefwnefefef\nowenewr");

	// Print the border
	TLib::printBorderWithAllColors(TERM_HEIGHT, TERM_WIDTH, BORDER_CHAR);

	TLib::moveCursorTo(10, 10);
	printf("Lines: %u\nColumns: %u\n", TERM_HEIGHT, TERM_WIDTH);

	//usleep(3000000);
	return 0;
}